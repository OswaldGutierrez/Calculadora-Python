class Operaciones:
    
    def opSumar(num1, num2):
        resultado = num1 + num2
        return resultado

    def opRestar(num1, num2):
        resultado = num1 - num2
        return resultado

    def opMultiplicar(num1, num2):
        resultado = num1 * num2
        return resultado

    def opDividir(num1, num2):
        resultado = num1 / num2
        return resultado
    

    